func (s *S) TestPlanAdd(c *check.C) {
	p := appTypes.Plan{
		Name:     "plan1",
		Memory:   9223372036854775807,
		Swap:     1024,
		CpuShare: 100,
	}
	err := SavePlan(p)
	c.Assert(err, check.IsNil)
	var plan appTypes.Plan
	err = s.conn.Plans().FindId(p.Name).One(&plan)
	c.Assert(err, check.IsNil)
	c.Assert(plan, check.DeepEquals, p)
}

func (s *S) TestPlanAddInvalid(c *check.C) {
	invalidPlans := []appTypes.Plan{
		{
			Memory:   9223372036854775807,
			Swap:     1024,
			CpuShare: 100,
		},
		{
			Name:     "plan1",
			Memory:   9223372036854775807,
			Swap:     1024,
			CpuShare: 1,
		},
		{
			Name:     "plan1",
			Memory:   4,
			Swap:     1024,
			CpuShare: 100,
		},
	}
	expectedError := []error{appTypes.PlanValidationError{"name"}, appTypes.ErrLimitOfCpuShare, appTypes.ErrLimitOfMemory}
	for i, p := range invalidPlans {
		err := SavePlan(p)
		c.Assert(err, check.FitsTypeOf, expectedError[i])
	}
}

